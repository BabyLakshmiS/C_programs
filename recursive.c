#include<stdio.h>
void even(int);
void odd(int);
int main()
{
    int n,i;
    scanf("%d",&n);
    int arr[n];
    for(i=0;i<n;i++)
    {
        scanf("%d",&arr[i]);
    }
    for(i=0;i<n;i++)
    {
        printf("&d",arr[i]);
    }
}
for(i=0;i<n;i++)
{
void even()
{
    if(arr[i]%2==0)
    {
        printf("%d",n);
    }
    return odd();
}
void odd()
{
    if(arr[i]%2!=0)
    {
        printf("%d",n);
    }
    return even();
}
}
